package com.example.linearlayoutactivity

class Aritmetica
{
    fun suma(nymeroUno: Float, nymeroDos: Float):Float{
        return nymeroUno + nymeroDos
    }
    fun resta(nymeroUno: Float, nymeroDos: Float):Float{
        return nymeroUno - nymeroDos
    }
    fun multiplicacion(nymeroUno: Float, nymeroDos: Float):Float{
        return nymeroUno * nymeroDos
    }
    fun division(nymeroUno: Float, nymeroDos: Float):Float{
        return nymeroUno / nymeroDos
    }

}